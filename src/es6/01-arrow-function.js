// Las arrow functions son una alternativa a las funciones comunes, con una sintaxis abreviada.

//Ejemplo de una funcion con sistaxis normal
function square (num) {
    return num * num;
}

// Ejemplo de la misma funcion con el metodo arrow
const square = (num) => {
    return num * num;
}

// Ejemplo de la misma funcion pero con el return implicito
const square = num => num * num;

// En los 3 casos tenemos la misma funcion escritas con distinta sintaxis.
